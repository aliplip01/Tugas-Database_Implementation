CREATE PROCEDURE  pFilmReport()
BEGIN
	SELECT F.TITLE, CONCAT(COUNT(T.FILM_ID), ' times')
    FROM FILM F
    LEFT JOIN TRANSACTION T ON T.FILM_ID = F.FILM_ID
    GROUP BY 1;
END $$
DELIMITER ;

call afl_satu.pFilmReport();

