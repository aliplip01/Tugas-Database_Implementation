DELIMITER $$
CREATE FUNCTION fCalculator(num1 int, op varchar(1), num2 int)
RETURNS INT DETERMINISTIC
BEGIN
	DECLARE HASIL INT;
    
     IF op = '+' THEN
        SET HASIL = num1 + num2;
    ELSEIF op = '-' THEN
        SET HASIL = num1 - num2;
    ELSEIF op = '*' THEN
        SET HASIL = num1 * num2;
    ELSEIF op = '/' THEN
		SET HASIL = num1 / num2;
	END IF;
    
    RETURN HASIL;
END $$
DELIMITER ;

select fCalculator(5, '*', 3) AS HASIL;

